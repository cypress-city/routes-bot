from modules.core import closeness


class Course:
    def __init__(self, abbrev: str, name: str):
        self.name = name
        self.abbrev = abbrev

    @property
    def full_display(self):
        return f"[{self.abbrev}] {self.name}"

    def closeness(self, user_input: str):
        term = user_input.lower()
        return closeness(term, self.abbrev.lower()) * 2 + closeness(term, self.name.lower())


